package main

import (
	"fmt"
	"time"
)

func main() {
	timestamps := []int64{
		1745911777, 1745911551, 1745911521, 1745911516, 1745911506,
		1745911505, 1745911411, 1745911041, 1745910892, 1745910273,
		1745909864, 1745894050, 1745824690, 1745811833, 1745808081,
		1745807328, 1745737094, 1745566304, 1745563272, 1745559838,
		1745459794, 1745459026, 1745457200, 1745401213, 1745381029,
		1745377548, 1745250876, 1745229115, 1745222573, 1745222156,
	}

	for i, ts := range timestamps {
		t := time.Unix(ts, 0) // 明确使用 UTC 时区
		fmt.Printf("%2d. %s (UTC)\n", i+1, t.Format("2006-01-02 15:04:05"))
	}

}